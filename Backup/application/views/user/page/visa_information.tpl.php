                        <p><strong>Visa on arrival</strong><br>
                          Business visa on arrival is available for citizens from 52 countries/territories flying into Yangon International Airport, Naypyidaw                        Airport or Mandalay International Airport using the application form and with company registration and invitation documents.</p>
                        <p><strong>eVisa</strong><br>
                          Myanmar launched the eVisa system on 1 September 2014. eVisas are issued online for tourism and business purposes only. An                        evisa is issued within 3 working days, is valid for 90 days from the date of issue and allows a stay for up to 28 days (for tourists) or 70 days (for business travellers). The fee for a business eVisa is US$70, while a tourist eVisa costs US$50.                        </p>
